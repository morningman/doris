<template>
  <div>
    <!-- 面包屑导航 -->
    <el-breadcrumb separator-class="el-icon-arrow-right">
      <el-breadcrumb-item :to="{ path: '/home/home' }">Home</el-breadcrumb-item>
      <el-breadcrumb-item :to="{ path: '/trans/Transactions' }">Transations</el-breadcrumb-item>
      <el-breadcrumb-item>Transations Operation</el-breadcrumb-item>
      <el-breadcrumb-item>Running</el-breadcrumb-item>
    </el-breadcrumb>
   <el-form :inline="true" :model="formInline" class="user-search">
   </el-form>
    <!--列表-->
    <el-table size="small" :data="listData" highlight-current-row v-loading="loading" border element-loading-text="拼命加载中" style="width: 100%;">
      <el-table-column align="center" type="selection" width="60">
      </el-table-column>
      <el-table-column sortable prop="TransactionId" label="TransactionId">
      </el-table-column>
      <el-table-column sortable prop="Label" label="Label" >
      </el-table-column>
      <el-table-column sortable prop="Coordinator" label="Coordinator" >
      </el-table-column>
      <el-table-column sortable prop="TransactionStatus" label="TransactionStatus" >
      </el-table-column>
      <el-table-column sortable prop="LoadJobSourceType" label="LoadJobSourceType" >
      </el-table-column>
      <el-table-column sortable prop="PrepareTime" label="PrepareTime" >
      </el-table-column>
      <el-table-column sortable prop="CommitTime" label="CommitTime" >
      </el-table-column>
      <el-table-column sortable prop="PublishTime" label="PublishTime" >
      </el-table-column>
      <el-table-column sortable prop="FinishTime" label="FinishTime" >
      </el-table-column>
      <el-table-column sortable prop="Reason" label="Reason" >
      </el-table-column>
      <el-table-column sortable prop="ErrorReplicasCount" label="ErrorReplicasCount" >
      </el-table-column>
      <el-table-column sortable prop="ListenerId" label="ListenerId" >
      </el-table-column>
      <el-table-column sortable prop="TimeoutMs" label="TimeoutMs" >
      </el-table-column>
      <el-table-column sortable prop="ErrMsg" label="ErrMsg" >
      </el-table-column>
    </el-table>
    <!-- 分页组件 -->
    <Pagination v-bind:child-msg="pageparm" @callFather="callFather"></Pagination>
  </div>
</template>

<script>
import { table_info_list } from '../../api/userMG'
import Pagination from '../../components/Pagination'
export default {
  data() {
    return {
      nshow: true, //switch开启
      fshow: false, //switch关闭
      loading: false, //是显示加载
      editFormVisible: false, //控制编辑页面显示与隐藏
      title: '添加',
      editForm: {
        deptId: '',
        deptName: '',
        deptNo: '',
        token: localStorage.getItem('logintoken')
      },
      formInline: {
        page: 1,
        limit: 10,
        varLable: '',
        varName: '',
        token: localStorage.getItem('logintoken')
      },
     
      userparm: [], //搜索权限
      listData: [], //用户数据
      // 分页参数
      pageparm: {
        currentPage: 1,
        pageSize: 10,
        total: 10
      }
    }
  },
  // 注册组件
  components: {
    Pagination
  },
  /**
   * 数据发生改变
   */

  /**
   * 创建完毕
   */
  created() {
    this.getdata(this.formInline)
    self = this
  },

  /**
   * 里面的方法只有被调用才会执行
   */
  methods: {
    // 获取Frontend列表
    getdata(parameter) {
      this.loading = true
      table_info_list(self.$route.params.id.replace(new RegExp(/\\/g),'\/'))
        .then(res => {
          this.loading = false
          if (res.success == false) {
            this.$message({
              type: 'info',
              message: res.msg
            })
          } else {
            this.listData = res.data
            // 分页赋值
            this.pageparm.currentPage = this.formInline.page
            this.pageparm.pageSize = this.formInline.limit
            this.pageparm.total = res.count
          }
        })
        .catch(err => {
          this.loading = false
          this.$message.error('菜单加载失败，请稍后再试！')
        })
    },
    // 分页插件事件
    callFather(parm) {
      this.formInline.page = parm.currentPage
      this.formInline.limit = parm.pageSize
      this.getdata(this.formInline)
    },
    // 关闭编辑、增加弹出框
    closeDialog() {
      this.editFormVisible = false
    }
  }
}
</script>

